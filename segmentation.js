function calculateBestSegment(likedNames) {
    // Крайні стани залишаємо (0, 1, 6-7 лайків)[cite: 4]
    if (likedNames.length === 0) return segmentProfiles["Гурман"];
    if (likedNames.length === 1) return segmentProfiles["Вибагливий"];
    if (likedNames.length >= 6) return segmentProfiles["Ентузіаст"];

    let finalScores = {};

    likedNames.forEach(name => {
        const dish = FOOD_DATA.find(d => d.name === name);
        if (dish && dish.weights) {
            for (const [category, weight] of Object.entries(dish.weights)) {
                // Додаємо вагу категорії, масштабовану до кількості лайків
                finalScores[category] = (finalScores[category] || 0) + (weight / likedNames.length);
            }
        }
    });

    let bestCategory = "Ентузіаст";
    let maxScore = 0;

    for (const [category, score] of Object.entries(finalScores)) {
        if (score > maxScore) {
            maxScore = score;
            bestCategory = category;
        }
    }

    if (maxScore <= 34) return segmentProfiles["Ентузіаст"];

    return segmentProfiles[bestCategory] || segmentProfiles["Ентузіаст"];
}