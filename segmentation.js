function calculateBestSegment(likedNames) {
    if (likedNames.length === 0) return SPECIAL_PROFILES["zero"];
    if (likedNames.length === 1) return SPECIAL_PROFILES["one"];
    if (likedNames.length >= 6) return SPECIAL_PROFILES["many"];

    // Рахуємо бали
    const scores = {};
    VALID_SEGMENTS.forEach(s => { scores[s] = 0; });
    likedNames.forEach(name => {
        const dish = FOOD_DATA.find(d => d.name === name);
        if (!dish) return;
        for (const [cat, w] of Object.entries(dish.weights)) {
            if (scores[cat] !== undefined) scores[cat] += w;
        }
    });

    // Сортуємо активні сегменти
    const ranked = VALID_SEGMENTS
        .filter(s => scores[s] > 0)
        .sort((a, b) => scores[b] - scores[a]);

    if (ranked.length === 0) return SPECIAL_PROFILES["zero"];

    const total = ranked.reduce((sum, s) => sum + scores[s], 0);
    const dominance = scores[ranked[0]] / total;

    // Чіткий одиночний лідер (≥60%)
    if (dominance >= 0.60) {
        return SINGLE_PROFILES[ranked[0]] || SPECIAL_PROFILES["many"];
    }

    // Шукаємо найкращий комбо-профіль по спадаючій кількості сегментів
    // (спочатку пробуємо взяти топ-3, потім топ-2)
    for (let take = Math.min(ranked.length, 3); take >= 2; take--) {
        const topN = ranked.slice(0, take);
        const topNScore = topN.reduce((s, seg) => s + scores[seg], 0);
        const topNShare = topNScore / total;

        // Топ-N має покривати ≥75% балів щоб комбо мало сенс
        if (topNShare >= 0.75) {
            const comboKey = [...topN].sort().join("+");
            if (COMBO_PROFILES[comboKey]) return COMBO_PROFILES[comboKey];
        }
    }

    // Якщо жоден комбо не підійшов — повертаємо відносного лідера
    return SINGLE_PROFILES[ranked[0]] || SPECIAL_PROFILES["many"];
}